metin = "Python"

# P Y T H O N
# 0 1 2 3 4 5

print(metin[5]) # index nosu 5 olan yani 6. karakteri yazar.

print(metin[-3]) # - ile index belirttigimiz zaman kelimeyi tersten sayar ve burda ilk karakter en sondan baslar.

print(metin[:3]) # ilk 3 karakteri alir (baslangictan itibaren)

print(metin[2:]) # ilk 2 karakteri vermez. 

print(metin[-2:]) #sondan ilk 2 karakteri verir.

print(metin[2:-1]) # ilk 2 karakteri ve sondaki ilk karakteri yazmaz. [2:5] diger bir yazilisi.



